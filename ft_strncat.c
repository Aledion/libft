#include "libft.h"

char	*ft_strncat(char *s1, char const *s2, size_t n)
{
	size_t	i;
	size_t	j;

	if ((!s1) || (!s2))
		return (NULL);
	i = 0;
	j = 0;
	while (s1[i] != 0)
		i++;
	while ((s2[j] != 0) && (j < n))
		s1[j++] = s2[j++];
	s1[i] = 0;
	return (s1);
}
