#include "libft.h"

char	*ft_strrchr(char const *s, int c)
{
	int		i;
	char	*p;

	if (!s)
		return (NULL);
	i = 0;
	p = NULL;
	while (s[i] != 0)
	{
		if (s[i] == c)
			p = (char *)&s[i];
		i++;
	}
	return (p);
}
