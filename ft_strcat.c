#include "libft.h"

char	*ft_strcat(char *s1, char const *s2)
{
	int	i;
	int	j;

	if ((!s1) || (!s2))
		return (NULL);
	i = 0;
	j = 0;
	while (s1[i] != 0)
		i++;
	while (s2[j] != 0)
		s1[i++] = s2[j++];
	s1[i] = s2[j];
	return (s1);
}
